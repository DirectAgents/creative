                <h2 class="faq-large-title light">General</h2>
                    <h3 class="bold cookies-title" id="general-1">What is Collapsed?</h3>
                    <p class="cookies-description">
                        Collapsed is a platform that collects and analyzes failed startups. Users are able to contribute failed startups to the platform and provide their own insights regarding why a particular startup failed.
                    </p>
                    <h3 class="bold cookies-title" id="general-2">How can I effectively use this site?</h3>
                    <p class="cookies-description">
                        Collapsed is suitable for people involved with startups & aspiring entrepreneurs. The best way to use this site would be to search for other startups in a similar position as yours and look into why they failed.
                         Doing so could potentially allow you to avoid the same mistakes as they did and ultimately increases the chance of your startup success.
                    </p>
                    <h3 class="bold cookies-title" id="general-3">How do you ensure all data is accurate?</h3>
                    <p class="cookies-description">
                        We require that every single submission includes a source that cites the startup's failure. If you're the founder of the startup, then you will need to make a public post acknowledging the closure of your startup and link it as a source.
                        <span class="bold">If you can't back up your submissions with legitimate sources, then it won't be published.</span>
                    </p>
                    <h3 class="bold cookies-title" id="general-4">What if Collapsed collapses?</h3>
                    <p class="cookies-description">
                        Hopefully someone out there adds us a to a failed startups directory.
                    </p>
                <div class="dropdown-menu-separator full-width"></div>

                <h2 class="faq-large-title light">User Accounts</h2>
                    <h3 class="bold cookies-title" id="user_accounts-1">What are the benefits of having an account?</h3>
                    <p class="cookies-description">
                        If you have an account on Collapsed then you'll be able to submit new failed startups, interact with content such as upvoting high quality answers. You'll also be one of our earliest adopters!
                    </p>
                    <h3 class="bold cookies-title" id="user_accounts-2">How do I sign up for an account?</h3>
                    <p class="cookies-description">
                        That's actually pretty easy. There's a "Sign In" button on the top right corner of this page. If you click it, you'll be able to sign in with your Facebook or Twitter account. We'll automatically create an account for you and use Facebook & Twitter for authentication.
                    </p>
                    <h3 class="bold cookies-title" id="user_accounts-3">Why can't I sign up using my email address?</h3>
                    <p class="cookies-description">
                        We don't currently support an "email & password" authentication login because <span class="bold">1)</span> that's one less password for you to remember & <span class="bold">2)</span> It's pretty easy to just log in with Facebook or Twitter.
                    </p>
                    <h3 class="bold cookies-title" id="user_accounts-4">How do I delete my account?</h3>
                    <p class="cookies-description">
                        We're sorry to see you go, if you want to delete your account just <a href="mailto:contact@collapsed.co" class="faq-subtitle-link bold" style="text-decoration: underline;"> click here </a>
                    </p>
                <div class="dropdown-menu-separator full-width"></div>

                <h2 class="faq-large-title light">Additional Information</h2>
                    <h3 class="bold cookies-title" id="additional_info-1">How can I advertise on this site?</h3>
                    <p class="cookies-description">
                        We currently don't offer any advertising services on our site, this may however change in the future.
                    </p>
                    <h3 class="bold cookies-title" id="additional_info-2">There's a startup with some inaccurate data, how do I correct this?</h3>
                    <p class="cookies-description">
                        If you find any inaccurate data on a startup featured on Collapsed, you can update the information by clicking the "Update" right next to that particular information.
                    </p>
                    <h3 class="bold cookies-title" id="additional_info-3">There's an answer containing inaccurate data about a startup, how do I report this?</h3>
                    <p class="cookies-description">
                        We understand that there are some people who may give an answer with inaccurate data or malicious intent. You can downvote these answers & report them.
                    </p>
            </div>
            <div class="col-md-6">
                <h2 class="faq-large-title light">Submitting a startup</h2>
                    <h3 class="bold cookies-title" id="submit_startup-1">How do I submit a startup?</h3>
                    <p class="cookies-description">
                        You can submit a startup by clicking the "+" button in the middle-top section of the screen.
                    </p>
                    <h3 class="bold cookies-title" id="submit_startup-2">Can I submit a startup even though I'm not the founder?</h3>
                    <p class="cookies-description">
                        You're free to submit any failed startups, regardless of whether you're the founder or not.
                    </p>
                    <h3 class="bold cookies-title" id="submit_startup-3">What happens after I've submitted a startup?</h3>
                    <p class="cookies-description">
                        Once you've submitted a startup, you should receive an email saying your submission was received. It will then go through a review process where one of our moderators review it to make sure all data is accurate. If everything's fine then your submission will be published.
                    </p>
                    <h3 class="bold cookies-title" id="submit_startup-4">I made a mistake on my submission, how do I correct it?</h3>
                    <p class="cookies-description">
                        If the startup isn't submitted yet then you can go to your dashboard and edit your submission from there, if it's published then you can go directly to the startup page and edit it from there.
                    </p>
                <div class="dropdown-menu-separator full-width"></div>

                <h2 class="faq-large-title light">Submitted startups</h2>
                    <h3 class="bold cookies-title" id="submitted_startup-1">It says my submission is "pending" what does this mean?</h3>
                    <p class="cookies-description">
                        It simply means that it hasn't been reviewed by one of our moderators yet.
                    </p>
                    <h3 class="bold cookies-title" id="submitted_startup-2">My submission has been stuck on "pending" for a while now?</h3>
                    <p class="cookies-description">
                        In the unlikely case that your startup hasn't been reviewed after 2 - 3 days, please contact us and we'll review it immediately.
                    </p>
                    <h3 class="bold cookies-title" id="submitted_startup-3">What happens if my submission is rejected?</h3>
                    <p class="cookies-description">
                        If your startup submission is rejected, it will usually come with a reason for why it was rejected and it will be placed in a rejected section for about 3 months. A rejected startup can't be submitted by any other user during those three months.
                    </p>
                    <h3 class="bold cookies-title" id="submitted_startup-4">What happens if my submission is accepted?</h3>
                    <p class="cookies-description">
                        The startup page will go live and you'll be able to view and add an answer to why you think that particular startup failed.
                    </p>
                <div class="dropdown-menu-separator full-width"></div>

                <h2 class="faq-large-title light">Submitting answers to why (startup name) failed</h2>
                    <h3 class="bold cookies-title" id="submit_answers-1">I wrote an answer but it was collapsed, why did this happen?</h3>
                    <p class="cookies-description">
                        Your answer is automatically collapsed if it's too short. Also other users may downvote or report your answer if they feel like the answer you provided was not appropriate.
                    </p>
                    <h3 class="bold cookies-title" id="submit_answers-2">I wrote an answer but I'm being limited by the 600 character limit, can it be increased?</h3>
                    <p class="cookies-description">
                        The 600 character limit is there so your answers can be as descriptive as you want it but yet allows readers to quickly digest information.
                    </p>
                    <h3 class="bold cookies-title" id="submit_answers-3">Why can't I add multiple reasons for why a startup failed?</h3>
                    <p class="cookies-description">
                        When writing your answer please try to look for the top reason for what cause the startup failure. If the startup failed due to multiple reasons there's a "Multiple Reason" option you can choose. You can only choose one reason because it forces you to really focus on determining the sole reason for a startup's failure. It also makes it easy to analyze later on.
                    </p>
                    <h3 class="bold cookies-title" id="submit_answers-4">How can I edit/delete my answer?</h3>
                    <p class="cookies-description">
                        You can go to the startup page or your profile and find your answer. If you scroll to the button of your answer, you'll notice an icon with three dots, if you click it you will be presented with the option to delete your answer.
                    </p>
